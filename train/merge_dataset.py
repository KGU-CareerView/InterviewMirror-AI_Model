# ── 1. AffectNet 전체 → Train ──
print("📂 AffectNet 전체를 Train으로 복사 중...")
aff_count = {'stable': 0, 'nervous': 0, 'neutral': 0}

for split in ['train', 'test']:
    split_path = os.path.join(ORIGINAL_DATA, split)
    if not os.path.exists(split_path):
        continue
    for emotion_folder in os.listdir(split_path):
        src = os.path.join(split_path, emotion_folder)
        if not os.path.isdir(src):
            continue
        mapped = AFFECTNET_MAP.get(emotion_folder.lower())
        if mapped is None:
            continue
        dst = os.path.join(MERGED_DATA, 'train', mapped)
        for f in os.listdir(src):
            shutil.copy2(os.path.join(src, f), os.path.join(dst, f"aff_{split}_{f}"))
            aff_count[mapped] += 1

print(f"✅ AffectNet 복사 완료:")
for cls, cnt in aff_count.items():
    print(f"   {cls}: {cnt}장")
print(f"   총합: {sum(aff_count.values())}장")

