import os
import cv2
import numpy as np
import mediapipe as mp
from PIL import Image
from tqdm import tqdm

mp_face = mp.solutions.face_detection

FER_ROOT = "/content/fer2013_raw/train"
OUTPUT_ROOT = "/content/drive/MyDrive/affectnet_project/processed_data_mediapipe"
IMG_SIZE = 224
MARGIN_RATIO = 0.2

LABEL_MAP = {
    'happy':    'stable',
    'neutral':  'neutral',
}

for cls in ['stable', 'nervous', 'neutral']:
    os.makedirs(os.path.join(OUTPUT_ROOT, cls), exist_ok=True)

print("✅ 설정 완료")

def crop_face_mediapipe(image_path, detector, margin=MARGIN_RATIO):
    img = cv2.imread(image_path)
    if img is None:
        return None

    h, w = img.shape[:2]
    rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
    results = detector.process(rgb)

    if not results.detections:
        return None

    detection = results.detections[0]
    bbox = detection.location_data.relative_bounding_box

    x = int(bbox.xmin * w)
    y = int(bbox.ymin * h)
    bw = int(bbox.width * w)
    bh = int(bbox.height * h)

    margin_x = int(bw * margin)
    margin_y = int(bh * margin)

    x1 = max(0, x - margin_x)
    y1 = max(0, y - margin_y)
    x2 = min(w, x + bw + margin_x)
    y2 = min(h, y + bh + margin_y)

    face = img[y1:y2, x1:x2]
    if face.size == 0:
        return None

    face = cv2.resize(face, (IMG_SIZE, IMG_SIZE), interpolation=cv2.INTER_LINEAR)
    return face

print("✅ 크롭 함수 준비 완료")

stats = {'success': 0, 'fail': 0}
class_counts = {'stable': 0, 'nervous': 0, 'neutral': 0}

with mp_face.FaceDetection(
    model_selection=1,
    min_detection_confidence=0.5
) as detector:

    for emotion_folder in os.listdir(FER_ROOT):
        emotion_path = os.path.join(FER_ROOT, emotion_folder)
        if not os.path.isdir(emotion_path):
            continue

        mapped_class = LABEL_MAP.get(emotion_folder.lower())
        if mapped_class is None:
            continue

        print(f"\n📂 {emotion_folder} → {mapped_class}")

        images = [f for f in os.listdir(emotion_path)
                  if f.lower().endswith(('.jpg', '.jpeg', '.png'))]

        for img_name in tqdm(images, desc=f"  {emotion_folder}"):
            img_path = os.path.join(emotion_path, img_name)
            face = crop_face_mediapipe(img_path, detector)

            if face is not None:
                save_name = f"ferplus_mp_{emotion_folder}_{img_name}"
                save_path = os.path.join(OUTPUT_ROOT, mapped_class, save_name)
                cv2.imwrite(save_path, face)
                stats['success'] += 1
                class_counts[mapped_class] += 1
            else:
                stats['fail'] += 1

print(f"\n{'='*50}")
print(f"✅ MediaPipe 크롭 완료!")
print(f"   성공: {stats['success']}장")
print(f"   실패: {stats['fail']}장")
print(f"\n📊 클래스별:")
for cls, cnt in class_counts.items():
    print(f"   {cls}: {cnt}장")